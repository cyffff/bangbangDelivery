package com.bangbang.kyc.model;

/**
 * Enum representing types of identification documents
 */
public enum DocumentType {
    PASSPORT,               // Passport
    NATIONAL_ID,            // National ID card
    DRIVERS_LICENSE,        // Driver's license
    RESIDENCE_PERMIT,       // Residence permit
    UTILITY_BILL,           // Utility bill (proof of address)
    BANK_STATEMENT,         // Bank statement (proof of address)
    TAX_CERTIFICATE,        // Tax certificate
    BUSINESS_REGISTRATION   // Business registration document
} 